  // combine color
  static Color combineMultipleColor(List<Color> colors) {
    int red = 0;
    int green = 0;
    int blue = 0;
    int alpha = 0;
    for (Color color in colors) {
      red += color.red;
      green += color.green;
      blue += color.blue;
      alpha += color.alpha;
    }
    return Color.fromARGB(
      alpha ~/ colors.length,
      red ~/ colors.length,
      green ~/ colors.length,
      blue ~/ colors.length,
    );
  }
